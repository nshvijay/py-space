# grades according to marks
marks= int(input("enter your marks:"))
if(marks>=90):
    print("S grade")
elif(90>marks>=80):
    print("A grade")
elif(80>marks>=70):
    print("B grade")
elif(70>marks>=60):
    print("C grade")
elif(60>marks>=50):
    print("D grade")
elif(50>marks>=40):
    print("E grade")
else:
    print("fail")
