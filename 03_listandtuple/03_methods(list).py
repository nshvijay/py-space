marks=[20,34,56,76,78,98]
marks.append(7) #adds the element at last index
print(marks)
marks.sort()    #sorts the list in ascending order
print(marks)
marks.sort(reverse=True) #sorts the list in desending order
print(marks)
marks.reverse()  #reverses the list 
print(marks)
marks.insert(7,56) #adds the given element at the given index
print(marks)
